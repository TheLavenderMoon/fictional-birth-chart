public class House {
    private double Cusp;
    private String Sign;
    private double Degree;

    public double getCusp() {
        return Cusp;
    }

    public void setCusp(double cusp) {
        Cusp = cusp;
    }

    public String getSign() {
        return Sign;
    }

    public void setSign(String sign) {
        Sign = sign;
    }

    public double getDegree() {
        return Degree;
    }

    public void setDegree(double degree) {
        Degree = degree;
    }
}
